package VeriTipleri;

public class TamSayi {
    public static void main(String[] Args){
        int kisaKenar = 10, uzunKenar = 20;
        int alan = kisaKenar * uzunKenar;
        int cevre = 2*(kisaKenar + uzunKenar);
        /*
        byte = 8 bit
        short = 16 bit
        integer = 32 bit
        long = 64 bit
         */
        System.out.println(alan);
        System.out.print(cevre);
    }
}